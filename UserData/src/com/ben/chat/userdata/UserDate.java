package com.ben.chat.userdata;

import java.io.Serializable;

/**
 *
 * @author bendaccache
 */
public class UserDate implements Serializable {
    private String day, month, year;
    public UserDate(String day, String month , String year){
        this.day=day;
        this.month=month;
        this.year=year;
    }

    @Override
    public String toString(){
        return day+"/"+month+"/"+year;
    }
}
